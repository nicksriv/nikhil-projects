package com.wavelabs.sb.response;

public class CustomResponse {

    private String id;
    private String name;
    private String type;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public CustomResponse(String id, String name, String type) {
        this.id = id;
        this.name = name;
        this.type = type;
    }

    @Override
    public String toString() {
        return "CustomResponse [id=" + id + ", name=" + name + ", type=" + type + "]";
    }

    public CustomResponse(){
        super();
    }
}
